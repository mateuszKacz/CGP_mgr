# TODO: Code binary operations


def bin_and(data):
    return data[0] + data[1]


def bin_subtract(data):
    return data[0] - data[1]


def bin_xor(data):
    return data[0] * data[1]


def bin_xand(data):
    return data[0] / data[1]
